--CREATE TABLE--

CREATE TABLE USERS (user_id				NUMBER(11) 		CONSTRAINT user_id_users_pk PRIMARY KEY,
					username	 		VARCHAR2(225)	CONSTRAINT username_users_nn NOT NULL CONSTRAINT username_uk UNIQUE,
					password			VARCHAR2(100)   CONSTRAINT password_users_nn NOT NULL,
					nama 				VARCHAR2(100)	CONSTRAINT nama_users_nn NOT NULL,
					alamat				VARCHAR2(100),
					email				VARCHAR2(25)	CONSTRAINT email_users_nn NOT NULL CONSTRAINT email_uk UNIQUE,
					telp				VARCHAR2(25),
					program_name 		VARCHAR2(100),
					created_date		DATE DEFAULT sysdate,
					created_by			VARCHAR2(100),
					updated_date		DATE DEFAULT sysdate,
					updated_by			VARCHAR2(100));
							 
							
CREATE TABLE MENU (	menu_id				NUMBER(11) 		CONSTRAINT menu_id_menu_pk PRIMARY KEY,
					nama				VARCHAR2(225) 	CONSTRAINT nama_menu_nn NOT NULL,
					icon 				VARCHAR2(50) ,
					url 				VARCHAR2(100),
					program_name 		VARCHAR2(100),
		    		created_date		DATE DEFAULT sysdate,
					created_by			VARCHAR2(100),
					updated_date		DATE DEFAULT sysdate,
					updated_by			VARCHAR2(200));
					
CREATE TABLE ROLES (role_id 				NUMBER(11) 		CONSTRAINT role_id_roles_pk PRIMARY KEY,
					nama					VARCHAR2(100) 	CONSTRAINT nama_role_roles_nn NOT NULL,
					program_name 			VARCHAR2(100),
			    	created_date			DATE DEFAULT sysdate,
					created_by				VARCHAR2(100),
					updated_date			DATE DEFAULT sysdate,
					updated_by				VARCHAR2(200));
					
				
CREATE TABLE ROLE_MENU (role_menu_id 		NUMBER(11) 		CONSTRAINT role_menu_id_role_menu_pk PRIMARY KEY,
						role_id 			NUMBER(11)		CONSTRAINT role_id_role_menu_fk REFERENCES ROLES(role_id),
						menu_id 			NUMBER(11)		CONSTRAINT menu_id_role_menu_fk REFERENCES MENU(menu_id),
						is_active 			VARCHAR2(10) ,
						program_name 		VARCHAR2(100),
			    		created_date		DATE DEFAULT sysdate,
						created_by			VARCHAR2(100),
						updated_date		DATE DEFAULT sysdate,
						updated_by			VARCHAR2(200));	
						

CREATE TABLE HAK_AKSES (hak_akses_id			NUMBER(11) 		CONSTRAINT hak_akses_id_hak_akses_nn PRIMARY KEY,
						user_id 				NUMBER(11)		CONSTRAINT user_id_hak_akses_fk REFERENCES USERS(user_id),
						role_id 				NUMBER(11)		CONSTRAINT role_id_hak_akses_fk REFERENCES ROLES(role_id),
						program_name 			VARCHAR2(100),
			    		created_date			DATE DEFAULT sysdate,
						created_by				VARCHAR2(100),
						updated_date			DATE DEFAULT sysdate,
						updated_by				VARCHAR2(200));
						
					
CREATE TABLE SUB_MENU (	sub_menu_id				NUMBER(11) 		CONSTRAINT sub_menu_id_sub_menu_pk PRIMARY KEY,
menu_id				NUMBER(11)				CONSTRAINT menu_id_menu_fk REFERENCES MENU(menu_id),
