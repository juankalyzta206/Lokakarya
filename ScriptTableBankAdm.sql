CREATE TABLE master_bank (
					norek NUMBER(7) CONSTRAINT norek_pk PRIMARY KEY,
					nama varchar2(20),
					alamat varchar2(30),
					notlp number(10),
					saldo number(12))
					user_id NUMBER(11);
				
CREATE TABLE transaksi_nasabah (ID_TRANSAKSI_NASABAH NUMBER(10) CONSTRAINT id_trnasabah_pk PRIMARY KEY,
						norek number(7) CONSTRAINT norek_fk REFERENCES master_bank (norek),
						tanggal DATE NOT NULL,
						status char(1) NOT null,
						uang number(12)NOT NULL,
						status_ket number(1),
						norek_dituju number(7),
						no_tlp number(9));
					
CREATE TABLE history_bank (
									id_history_bank NUMBER(10) CONSTRAINT id_historybnk_pk PRIMARY KEY,
									tanggal DATE NOT NULL,
									norek number(7) CONSTRAINT norek_fk2 REFERENCES master_bank (norek), 
									status_ket number(1),
									nama varchar2(50),
									uang number(20),
									norek_dituju number(7),
									no_tlp number(20));
									nama_tujuan number(50)

		
--seq
CREATE SEQUENCE MASTERBANK_SEQ
INCREMENT BY 1 START WITH 120
MAXVALUE 9999 
NOCACHE 
NOCYCLE;
			
CREATE SEQUENCE TRANSFER_NASABAH_SEQ
INCREMENT BY 1 START WITH 120
MAXVALUE 9999 
NOCACHE 
NOCYCLE;

